function shuffle(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function drawCard(type, lvl) {
  let source, deck;

  if (type === "truth" && lvl === "soft") {
    source = truthsSoft; deck = deckTruthSoft;
  } else if (type === "truth" && lvl === "hot") {
    source = truthsHot; deck = deckTruthHot;
  } else if (type === "truth") {
    source = truthsExtreme; deck = deckTruthExtreme;
  } else if (type === "dare" && lvl === "soft") {
    source = daresSoft; deck = deckDareSoft;
  } else if (type === "dare" && lvl === "hot") {
    source = daresHot; deck = deckDareHot;
  } else {
    source = daresExtreme; deck = deckDareExtreme;
  }

  if (deck.length === 0) deck.push(...shuffle([...source]));
  return deck.pop();
}

function playTOD(type) {
  const lvl = document.getElementById("todLevel")?.value;
  const card = document.getElementById("todCard");
  const text = document.getElementById("todText");
  if (!card || !text) return;

  const arr =
    type === "truth"
      ? truthsSoft
      : daresSoft;

   // RESET TOTAL
  card.classList.remove("flip");
  card.classList.add("reset");
  
  // force reflow
  void card.offsetWidth;

  // set text
  text.innerText = arr[Math.floor(Math.random() * arr.length)];

  // flip ulang
  card.classList.remove("reset");
  card.classList.add("flip");
}

